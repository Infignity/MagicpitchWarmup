import { createSlice } from "@reduxjs/toolkit"
import { RootState } from "../store"
import { sampleWarmups } from "@/app/constants"

const initialState: {
    warmups: typeof sampleWarmups
} = {
    warmups: [...sampleWarmups]
}

const warmupsSlice = createSlice({
    name: "warmups",
    initialState,
    reducers: {
        setWarmups: (state, action) => {
            state.warmups = action.payload
        },
    }
})


export const { setWarmups } = warmupsSlice.actions
export default warmupsSlice.reducer

export function selectWarmups(state: RootState) {
    return state.warmups.warmups
}
